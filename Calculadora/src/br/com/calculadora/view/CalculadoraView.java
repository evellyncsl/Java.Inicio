    private void maisEmenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maisEmenosActionPerformed

        try {
            String currentText = resultado.getText();
            if (currentText != null && !currentText.trim().isEmpty()) {
                double numDisplay = Double.parseDouble(currentText);
                numDisplay *= -1;
                resultado.setText(Double.toString(numDisplay));
            }
        } catch (NumberFormatException ex) {
            System.err.println("Erro ao mudar o sinal: Conteúdo inválido no display. " + ex.getMessage());
        }
    }//GEN-LAST:event_maisEmenosActionPerformed

