    private void maisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maisActionPerformed
        calculadora.setValor1(Double.parseDouble(resultado.getText()));
        segundoValor = true;
        resultado.setText("");
        operacao = "soma";
    }//GEN-LAST:event_maisActionPerformed

    private void igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualActionPerformed
        if (segundoValor == true) {
            calculadora.setValor2(Double.parseDouble(resultado.getText()));
            if (operacao == "soma") {
                calculadora.somar();
                resultado.setText(String.valueOf(calculadora.getResultado()));
                operacao = "";
            }
            if (operacao == "multiplicacao") {
                calculadora.multiplicacao();
                resultado.setText(String.valueOf(calculadora.getResultado()));
                operacao = "";
            }
            if (operacao == "divisao") {
                calculadora.divisao();
                resultado.setText(String.valueOf(calculadora.getResultado()));
                operacao = "";
            }
            if (operacao == "subtracao") {
                calculadora.subtracao();
                resultado.setText(String.valueOf(calculadora.getResultado()));
                operacao = "";
            }
        }
    }//GEN-LAST:event_igualActionPerformed

